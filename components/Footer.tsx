const Footer: React.FC = () => {
  const [content, setContent] = useState<FooterContent>(defaultFooterContent);

  useEffect(() => {
    const fetchFooterContent = async () => {
      try {
        const docRef = doc(db, 'settings', 'content');
        const docSnap = await getDoc(docRef);
        if (docSnap.exists() && docSnap.data().footer) {
          const footerData = docSnap.data().footer;
          setContent({
            copyright: footerData.copyright || defaultFooterContent.copyright,
            links: footerData.links || defaultFooterContent.links,
            socials: footerData.socials || defaultFooterContent.socials
          });
        }
      } catch (error) {
        console.error('Error fetching footer content:', error);
      }
    };
    fetchFooterContent();
  }, []);

  return (
    <footer className="bg-[#0a0a0a] py-6">
      <div className="max-w-7xl mx-auto px-6 flex flex-col md:flex-row justify-between items-center gap-4">
        {/* Left: Copyright */}
        <p className="text-gray-500 text-sm">
          {content.copyright}
        </p>

        {/* Center: Links */}
        {content.links.length > 0 && (
          <div className="flex items-center gap-6">
            {content.links.map((link) => (
              <a
                key={link.id}
                href={link.url}
                className="text-gray-500 text-sm hover:text-white transition-colors underline"
              >
                {link.label}
              </a>
            ))}
          </div>
        )}

        {/* Right: Social Icons */}
        {content.socials.length > 0 && (
          <div className="flex items-center gap-5">
            {content.socials.map((social) => (
              <a
                key={social.id}
                href={social.url}
                className="text-gray-500 hover:text-white transition-colors"
                target="_blank"
                rel="noopener noreferrer"
              >
                {getSocialIcon(social.platform)}
              </a>
            ))}
          </div>
        )}
      </div>
    </footer>
  );
};

export default Footer;